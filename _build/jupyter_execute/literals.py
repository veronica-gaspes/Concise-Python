# ## String literals
# 
# Strings are used to deal with text in programs.

# In[12]:


'Hello to you!'


# In[13]:


# Notice: by combining '' and "" you can include these in the text!

"Hello to 'you'!"


# In[14]:


'Hello to "you"!'


